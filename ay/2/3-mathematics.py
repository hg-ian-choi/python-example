############ mathematics ############
# Rule: PEMDAS
#   Parentheses               => ()
#   Exponents                 => **
#   Multiplication / Division => * / /
#   Addition / Subtraction    => + / -
addition = 3 + 5
print(addition)  # int

subtraction = 7 - 3
print(subtraction)  # int

multiplication = 3 * 2
print(multiplication)  # int

division = 6 / 3
print(division)  # float

exponent = 2**3
print(exponent)  # int
