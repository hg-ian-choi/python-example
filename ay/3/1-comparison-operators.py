# Comparison Operators
#   <  : Less than
#   <= : Less than or equal to
#   >  : Greater than
#   >= : Greater than or equal to
#   == : Equal to
#   != : Not equal to

print(1 < 2)   # True
print(1 <= 2)  # True
print(1 > 2)   # False
print(1 >= 2)  # False
print(1 == 2)  # False
print(1 != 2)  # True
